public class ForloopExample {

	public static void main(String[] args) {
		
	for(int a=10;a>=1;a--){		   
		   System.out.println("the value of a is: " +a);
		  }

			  for(int i=1;i<=20;i++)
			  {
		   if(i==7 || i==10){
		    continue;
			   }
			   System.out.println("the value of i is: "  +i);
			  }
	}

	}
